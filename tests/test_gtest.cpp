#include <gtest/gtest.h>

/**
 * @brief Базовый тест для проверки работы Google Test
 *
 * Простой тест, который проверяет:
 * - Корректность настройки тестовой среды
 * - Работу базовых assertion'ов Google Test
 * - Правильность компиляции и линковки тестов
 *
 * Этот тест должен всегда проходить успешно.
 */
TEST(HelloTest, BasicAssertions) {
    // Проверяем, что строки не равны
    EXPECT_STRNE("hello", "world");

    // Проверяем простое арифметическое выражение
    EXPECT_EQ(7 * 6, 42);
}